    public void preencherTabelaProdutos(String sql) {
        ArrayList dadosUsuario = new ArrayList();
        String colunas[] = new String[]{"ID", "Nome", "$ Compra", "$ Venda", "QTD", "Fornecedor", "Categoria", "Receita"};

        // abre a conexao
        conexaoBD.conectaBD();
        conexaoBD.executaSql(sql);

        try {
            // pega o primeiro resultado encontrado na tabela
            conexaoBD.rs.first();

            // mostra todos os dados da tabela
            do {
                dadosUsuario.add(new Object[]{
                    conexaoBD.rs.getInt("idproduto"),
                    conexaoBD.rs.getString("nomeproduto"),
                    conexaoBD.rs.getDouble("valorcompra"),
                    conexaoBD.rs.getDouble("valorvenda"),
                    conexaoBD.rs.getString("quantidade"),
                    conexaoBD.rs.getString("fornecedor"),
                    conexaoBD.rs.getString("categoria"),
                    conexaoBD.rs.getString("receita")

                });
            } while (conexaoBD.rs.next());// enquanto houver dados
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher ArrayList.\nErro: " + ex);
        }

// cria uma intancia do modelo de tabela aqui nos passamos todos os objetos e o t√≠tulo de cada uma das colunas
        ModeloTabelaX modeloTabela = new ModeloTabelaX(dadosUsuario, colunas);

        // seta o modelo da tabela 
        jTableProduto.setModel(modeloTabela);

        // ID
        jTableProduto.getColumnModel().getColumn(0).setPreferredWidth(30);
        jTableProduto.getColumnModel().getColumn(0).setResizable(false);

        // NOME
        jTableProduto.getColumnModel().getColumn(1).setPreferredWidth(85);
        jTableProduto.getColumnModel().getColumn(1).setResizable(false);

        // VALOR COMPRA
        jTableProduto.getColumnModel().getColumn(2).setPreferredWidth(70);
        jTableProduto.getColumnModel().getColumn(2).setResizable(false);

        // VALOR VENDA
        jTableProduto.getColumnModel().getColumn(3).setPreferredWidth(70);
        jTableProduto.getColumnModel().getColumn(3).setResizable(false);

        // QTD
        jTableProduto.getColumnModel().getColumn(4).setPreferredWidth(35);
        jTableProduto.getColumnModel().getColumn(4).setResizable(false);

        // FORNECEDOR
        jTableProduto.getColumnModel().getColumn(5).setPreferredWidth(120);
        jTableProduto.getColumnModel().getColumn(5).setResizable(false);

        // CATEGORIA
        jTableProduto.getColumnModel().getColumn(6).setPreferredWidth(85);
        jTableProduto.getColumnModel().getColumn(6).setResizable(false);

        // RECEITA
        jTableProduto.getColumnModel().getColumn(7).setPreferredWidth(55);
        jTableProduto.getColumnModel().getColumn(7).setResizable(false);

        // n√£o deixa reordenar os dados da tabela
        jTableProduto.getTableHeader().setReorderingAllowed(false);

        // n√£o deixa a tabela ser redimensionada
        jTableProduto.setAutoResizeMode(jTableProduto.AUTO_RESIZE_OFF);

        // permite selecionar apenas um dado na tabela por vez
        jTableProduto.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        // fecha a conex√£o
        conexaoBD.desconectaBD();
    }

